package com.grupoatrium.modelo;

public class LibrosAutores {
	
	private int id;
	private Libro libro;
	private Autor autor;
	
	public LibrosAutores() {}
	
	public LibrosAutores(int id, Libro libro, Autor autor) {
		super();
		this.id = id;
		this.libro = libro;
		this.autor = autor;
	}



	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Libro getLibro() {
		return libro;
	}
	public void setLibro(Libro libro) {
		this.libro = libro;
	}
	public Autor getAutor() {
		return autor;
	}
	public void setAutor(Autor autor) {
		this.autor = autor;
	}
	@Override
	public String toString() {
		return "LibrosAutores [id=" + id + ", libro=" + libro + ", autor=" + autor + "]";
	}
	
	
	

}
